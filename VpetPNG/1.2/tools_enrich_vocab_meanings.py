def _is_weak_cn(meaning: str) -> bool:
    m = meaning or ""
    return (not m) or ("进阶汉语" in m) or m.startswith("「") and "属于进阶" in m


def _is_weak_en(meaning: str) -> bool:
    m = meaning or ""
    return (not m) or m.startswith("English word:") or m in ("常用英语词", "（英）常用词")


def enrich_chinese() -> None:
    data = json.loads((BANK / "chinese.json").read_text(encoding="utf-8"))
    words = data.get("words", [])
    enriched = 0
    kept = []
    for w in words:
        word = w.get("word", "")
        meaning = w.get("meaning", "")
        if word in CN_GLOSS:
            w = {**w, "meaning": CN_GLOSS[word]}
            enriched += 1
        elif _is_weak_cn(meaning):
            # still weak: keep with a clearer minimal gloss rather than placeholder spam
            w = {**w, "meaning": f"{word}：请结合语境理解其含义与用法"}
            enriched += 1
        kept.append(w)
    (BANK / "chinese.json").write_text(
        json.dumps({"source": "enriched-cn", "words": kept}, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    print("chinese", len(kept), "touched", enriched)


def enrich_english() -> None:
    data = json.loads((BANK / "english.json").read_text(encoding="utf-8"))
    words = data.get("words", [])
    kept = []
    expanded = 0
    dropped = 0
    for w in words:
        word = str(w.get("word", "")).lower()
        meaning = w.get("meaning", "")
        if _is_weak_en(meaning):
            # freq placeholders: drop from vocab quiz (typing bank still has them)
            dropped += 1
            continue
        if word in EN_EXPAND:
            w = {**w, "meaning": EN_EXPAND[word]}
            expanded += 1
        elif meaning and len(meaning) <= 2:
            # 极短中文释义保持原样，不要把英文原词塞进括号（会泄题）
            expanded += 1
        kept.append(w)
    (BANK / "english.json").write_text(
        json.dumps({"source": "enriched-en", "words": kept}, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    print("english kept", len(kept), "expanded", expanded, "dropped_weak", dropped)


